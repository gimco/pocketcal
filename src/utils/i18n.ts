// Función para obtener una traducción
export const t = (key: keyof typeof translations.en, language?: string): string => {
	const lang = language || getBrowserLanguage();
	const translation = translations[lang as keyof typeof translations] || translations.en;
	return translation[key] || translations.en[key];
};

// Función para obtener todas las traducciones de un idioma
export const getTranslations = (language?: string) => {
	const lang = language || getBrowserLanguage();
	return translations[lang as keyof typeof translations] || translations.en;
};
